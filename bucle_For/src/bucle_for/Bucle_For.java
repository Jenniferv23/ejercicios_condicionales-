
package bucle_for;


public class Bucle_For {

   
    public static void main(String[] args) {
        /*
        Bucle For, crea una variable numeroFor, esta variable tendrà como valor
        0 y su condiciòn serà que la variable sea igual o menor que 3, se irà 
        incrementando en 1 su valor cada vez que se ejecute y deberà mostrarse por
        pantalla. 
        */
        
        for (int i=0; i<=3; i++) {
            System.out.println(i);
        }
   
    
    }
    
}
